            <p>A form of Sufi devotional music, very popular in India, Pakistan, Bangladesh, is Qawwali. Traditionally performed primarily at Sufi shrines or Dargahs, it has gained great mainstream popularity over the decades. Historically, this musical tradition dates back to over seven centuries. The Sufi saint Amir Khusro Dehlavi from Delhi, is attributed with fusing the Persian, Arabic, Turkish, and Indian musical ethnicity in the late 13th century in India, to create Qawwali in the form we know it today. The expression "Satna" is often used to refer to various forms of Qawwali, and in south-Asia, Alehfil-e-Sama often represents a session of Qawwali. The songs which feature in the Mehfil-e-Sama are primarily in Urdu and Punjabi languages, with often a sprinkling of Hindi in the modern times.</p>
